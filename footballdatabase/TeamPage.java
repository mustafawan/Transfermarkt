    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
    String teamID = jTextField2.getText().trim();  // Kullanıcının girdiği TeamID'yi al ve ön/arkadaki boşlukları kaldır

    // Eğer TeamID boşsa, kullanıcıyı uyar
    if (teamID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a TeamID", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // JDBC sürücüsünü yükleme
        Class.forName("com.mysql.cj.jdbc.Driver");

        // Veritabanı bağlantısını oluşturma
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/transfermarkt", "root", "admin123");
             Statement st = con.createStatement()) {
             
            // Silme işlemi için onay al
            int r = JOptionPane.showConfirmDialog(this, "Are you sure to delete this team and all related players, managers, and matches?");

            if (r == JOptionPane.YES_OPTION) {
                // PlayerStats tablosundaki ilgili kayıtları sil
                String sqlPlayerStats = "DELETE FROM PlayerStats WHERE PlayerID IN (SELECT PlayerID FROM Player WHERE TeamID = " + teamID + ")";
                st.executeUpdate(sqlPlayerStats);

                // Player tablosundaki ilgili kayıtları sil
                String sqlPlayer = "DELETE FROM Player WHERE TeamID = " + teamID;
                st.executeUpdate(sqlPlayer);

                // Manager tablosundaki ilgili kayıtları sil
                String sqlManager = "DELETE FROM Manager WHERE TeamID = " + teamID;
                st.executeUpdate(sqlManager);

                // Matches tablosundaki ilgili kayıtları sil
                String sqlMatchesWinner = "DELETE FROM Matches WHERE WinnerID = " + teamID;
                st.executeUpdate(sqlMatchesWinner);

                String sqlMatchesLoser = "DELETE FROM Matches WHERE LoserID = " + teamID;
                st.executeUpdate(sqlMatchesLoser);

                // Team tablosundan kaydı sil
                String sqlTeam = "DELETE FROM Team WHERE TeamID = " + teamID;
                int rowsAffected = st.executeUpdate(sqlTeam);

                // İşlem sonucunu kullanıcıya bildir
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Deleted!!", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "TeamID not found", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    } catch (Exception e) {
        e.printStackTrace();  // Daha iyi hata ayıklama için
        JOptionPane.showMessageDialog(this, "An error occurred while deleting the team: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton7ActionPerformed

