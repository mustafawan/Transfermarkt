    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String inputID = txtINPUTID.getText().trim();
        String leagueID = txtID.getText().trim();
        String country = txtCountry.getText().trim();
        String lname = txtLNAME.getText().trim();

        // Eğer gerekli alanlar boşsa, kullanıcıyı uyar
        if (inputID.isEmpty() || leagueID.isEmpty() || country.isEmpty() || lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // JDBC sürücüsünü yükleme
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Veritabanı bağlantısını oluşturma
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/transfermarkt", "root", "admin123");
            Statement st = con.createStatement();

            int r = JOptionPane.showConfirmDialog(this, "Are you sure to update this league?");
            if (r == 0) {
                // SQL UPDATE sorgusunu hazırlama ve çalıştırma
                Boolean b = st.execute("UPDATE League SET LeagueID = '" + leagueID + "', Country = '" + country + "', LName = '" + lname + "' WHERE LeagueID = '" + inputID + "'");

                if (!b) {
                    JOptionPane.showMessageDialog(this, "Updated");
                } else {
                    JOptionPane.showMessageDialog(this, "Something went wrong");
                }
            }

            // Veritabanı bağlantısını kapatma
            con.close();
        } catch (Exception e) {
            e.printStackTrace();  // Daha iyi hata ayıklama için
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

