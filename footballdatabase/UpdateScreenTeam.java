    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
               String inputID = txtINPUTID.getText().trim();

        // Eğer gerekli alan boşsa, kullanıcıyı uyar
        if (inputID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Team ID", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // JDBC sürücüsünü yükleme
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Veritabanı bağlantısını oluşturma
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/transfermarkt", "root", "admin123");
            Statement st = con.createStatement();

            // SQL SELECT sorgusunu hazırlama ve çalıştırma
            ResultSet rs = st.executeQuery("SELECT TeamID, TeamName, Country, LeagueID FROM Team WHERE TeamID='" + inputID + "'");

            // Sonuç kümesini işleyin ve JTextField bileşenlerine yerleştirin
            if (rs.next()) {
                txtTID.setText(rs.getString("TeamID"));
                txtTeamName.setText(rs.getString("TeamName"));
                txtCountry.setText(rs.getString("Country"));
                txtLID.setText(rs.getString("LeagueID"));
            } else {
                JOptionPane.showMessageDialog(this, "No team found with the given ID", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Sonuç kümesini ve ifadesini kapat
            rs.close();
            st.close();

            // Veritabanı bağlantısını kapat
            con.close();
        } catch (Exception e) {
            e.printStackTrace();  // Daha iyi hata ayıklama için
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

