    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String inputID = txtINPUTID.getText().trim();
        String teamID = txtTID.getText().trim();
        String teamName = txtTeamName.getText().trim();
        String country = txtCountry.getText().trim();
        String leagueID = txtLID.getText().trim();

        // Eğer gerekli alanlar boşsa, kullanıcıyı uyar
        if (inputID.isEmpty() || teamID.isEmpty() || teamName.isEmpty() || country.isEmpty() || leagueID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // JDBC sürücüsünü yükleme
            Class.forName("com.mysql.cj.jdbc.Driver");

            // Veritabanı bağlantısını oluşturma
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/transfermarkt", "root", "admin123");
            Statement st = con.createStatement();

            int r = JOptionPane.showConfirmDialog(this, "Are you sure to update this team?");
            if (r == 0) {
                // SQL UPDATE sorgusunu hazırlama ve çalıştırma
                String sql = "UPDATE Team SET TeamID = '" + teamID + "', TeamName = '" + teamName + "', Country = '" + country + "', LeagueID = '" + leagueID + "' WHERE TeamID = '" + inputID + "'";
                int affectedRows = st.executeUpdate(sql);
                boolean b = st.execute(sql);

                if (!b) {
                    JOptionPane.showMessageDialog(this, "Team updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Update failed!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

            // Veritabanı bağlantısını kapatma
            con.close();
        } catch (Exception e) {
            e.printStackTrace();  // Daha iyi hata ayıklama için
            JOptionPane.showMessageDialog(this, "An error occurred: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

